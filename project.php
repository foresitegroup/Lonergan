          The purpose of the Bernard Lonergan Archive project is to contribute to the preservation and communication of Lonergan's work, by adding to the publication of his collected works access to his most important papers and to his recorded lectures.<br>
          <br>
          <strong>Archive Provenance</strong><br>
          Robert M. Doran started working on the papers in 1985.  The original papers, which are the property of the Jesuits in English Canada, are preserved at the Lonergan Research Institute, Toronto.  Doran, who is a Trustee of the Bernard Lonergan Estate at the request of Lonergan and with the approval of the Jesuits in English Canada, first produced a catalogue of most of the papers in Microsoft Word, and John Dadosky completed the catalogue.  The catalogue was then re-transcribed into an archival database, and the database has been converted to an Excel file, which forms the basis for accessing the present information.<br>
          <br>
          <img src="images/project1.jpg" alt="Bernard" style="float: left; padding: 0px 3px 0px 0px;">
          Some of the papers that will appear in the Archive have been scanned from microfilms made from the original papers, but some have been scanned from photocopies. Most have been converted into searchable PDF files, but handwritten materials are available in non-searchable format.  Many of the PDF files will eventually be complemented by Word files that in some instances will be easier to read. In fact a number of these files have already been done by Robert Doran, while he was cataloguing the materials.  The Word files will also attempt to present deciphered versions of Lonergan's handwriting.<br>
          <br>
          The audio files are downloadable mp3 files created by Greg Lauzon.  The recordings were originally made onto reel-to-reel tapes and cassettes.  These were transferred to digital format, and have been cleaned up as much as possible.<br>
