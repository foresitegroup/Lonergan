      <strong>Philosophy/Theology of Bernard Lonergan</strong><br>
      Bernard Lonergan is regarded by many as one of the greatest philosophical and theological minds of the twentieth century.  In this initial introduction, adapted from one that Robert Doran wrote several years ago, we can focus on the following three points.<br>
      <img src="images/bio3.jpg" alt="" style="float: left; padding: 8px 3px 0px 0px;">
      <br>
      <strong>(1) What am I doing when I am knowing?</strong><br>
      The first eleven chapters of Lonergan's book <em>Insight</em> - his monumental work - are an attempt to answer this question. Why did he ask this question? There are at least two reasons:<br>
      &nbsp; &nbsp; <strong>(a)</strong> to provide what he called a 'common ground' on which people could meet one another, that is, the common ground of the operations through which they pursue meaning and truth, and<br>
      &nbsp; &nbsp; <strong>(b)</strong> to provide what is probably a solution to the fragmentation of knowledge - not by attempting to integrate the content of knowledge, which is not possible, but by acknowledging the same operations of experiencing, understanding, and judging in all fields. This acknowledgment, which Lonergan's book <em>Insight</em> shows, brings a startling unity to knowledge and to the pursuit of understanding in every field. It helps us relate such 'hard sciences' as mathematics, physics, and chemistry to the sciences of life, and to relate all of these to psychology, philosophy, the arts, and theology.<br>
      <br>
      The key is the act of insight. Lonergan is seeking an insight into insight itself. He writes: "...to grasp it in its conditions, its working, and its results is to confer a basic yet startling unity on the whole field of human inquiry and human opinion."<br>
      <br>
      <strong>(2) What difference does this make?</strong><br>
      From the study of insight, Lonergan tackles some of the major challenges of our time, all of which are in one way or another a function of biases that interfere with the operations of experiencing, understanding, and judging. So not only does he show us many different kinds of insights; he also displays several kinds of bias that interfere with insight and prevent us from being attentive, intelligent, reasonable, and responsible. There is the psychic bias that comes from trauma, the individual bias of the egoist, the group bias of the privileged classes, and a general bias that we all have against asking the hard questions and facing the ultimate issues and long-range consequences of our actions. All of these are spelled out in intricate detail in his work.<br>
      <br>
      <img src="images/bio4.jpg" alt="" style="float: right; padding: 0px 0px 0px 6px;">
      Another difference is that from his study of experiencing, understanding, judging, and (to add another set of operations) deciding (E-U-J-D), Lonergan goes on to show us how various academic disciplines can be 'mapped' according to what he calls functional specialties: to each level of consciousness (E-U-J-D) there corresponds a whole set of operations both for understanding the previous history of the discipline and for moving creatively into the future.<br>
      <br>
      <strong>(3) Is this all there is?</strong><br>
      Many of the most pressing problems of our own time have to do with religion. Religion is at the heart of a great deal of violence and persecution. It also is the ultimate source of most of what is good in the human world. Is there any way of discriminating authentic from inauthentic religion? Is there a way of providing a ground for inter-religious dialogue in the pursuit of peace? These are issues to which Lonergan gave increasing attention in his last years. Stay tuned, because these are the areas where his thought is going to give rise to a number of wonderful developments.<br>
      <br>
      Lonergan did not just expand the fields of Theology and Philosophy. He also applied his tenets of thought to Economics. Many people have had the experience on reading Lonergan, "This is worth a lifetime." This website is the result of that experience. Perhaps now we can help others have the same experience.<br>
